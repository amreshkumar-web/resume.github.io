
var show=document.getElementById("HIDE");
var hide=document.getElementById("fhide");
var info=document.getElementById("ginfo2")
function change(){
    hide.className=("fhide2");
    show.className="HIDE2";
    info.className="ginfo";
}
function back(){
    show.className="HIDE";
    hide.className="fhide";
    info.className="ginfo2";
}
